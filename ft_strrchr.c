#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	const char	*last;

	last = NULL;
	while (*s)
	{
		if (*s == (char)c)
			last = s;
		s++;
	}
	if (last)
		return ((char *)last);
	if (c == '\0')
		return ((char *)s);
	return (NULL);
}
